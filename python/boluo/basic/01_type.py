# 变量和数据类型

# 1.数字类型
# 整型
num1 = 101
num2 = -239
num3 = 0x80
num4 = 123456789

# 浮点型
num5 = 3.14
num6 = 4.2E-10
num7 = -2.34E-19

# 复数类型
num8 = 3.12 + 1.23j
num9 = -1.23 - 98j

# 2.布尔类型
True
False

# 3.字符串类型
string = "python"

# 4.列表和元组类型
# 我们可以将列表和元组当作 "数组", 他们可以保存任意数量的任意类型的元素
# 列表中的元素使用[]包含, 元素个数和值可以随意修改
# 元组中的元素使用()包含, 元素不可以被修改
list_name = [1, 2, "hello"]
tuple_name = (1, 2, "hello")

# 5.字典类型
# 字典是python中的映射数据类型, 由键值对组成
dict_name = {"name": "dingc", "age": 18}

# 查看变量类型
type(dict_name)